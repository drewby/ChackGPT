namespace chackgpt.Web.Models;

/// <summary>
/// Represents the emotional states that DrewGPT can display
/// </summary>
public enum DrewEmotion
{
    /// <summary>
    /// Default neutral expression - use for general conversation
    /// </summary>
    Neutral,
    
    /// <summary>
    /// Happy expression - use when in good spirits or joking around
    /// </summary>
    Happy,
    
    /// <summary>
    /// Excited expression - use when particularly enthusiastic or energized
    /// </summary>
    Excited,
    
    /// <summary>
    /// Scared expression - use when discussing something concerning or dramatic
    /// </summary>
    Scared
}
